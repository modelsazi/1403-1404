clc; clear; close all

J = @(x) [-1-4.5*x(2)   -4.5*x(1)     6*x(3) 
          -4.5*x(2)     -1-4.5*x(1)   6*x(3) 
           4.5*x(2)      4.5*x(1)     -1-6*x(3)];
b = @(x) -[2+2/3*x(1)-5/3*x(1)-3*x(1)*x(2)*1.5+3*x(3)^2
           2+2/3*x(2)-5/3*x(2)-3*x(1)*x(2)*1.5+3*x(3)^2
           2/3*x(3)-5/3*x(3)+3*x(1)*x(2)*1.5-3*x(3)^2];
err = 1; x0 = [1.5;1.5;1.5];
while err > 1e-4
    dx = J(x0)\b(x0);
    x0 = x0 + dx;
    err = max(abs(dx));
end
